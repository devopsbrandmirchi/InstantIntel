GRANT SELECT, INSERT, UPDATE, DELETE ON public.sales_processing_queue TO service_role;
GRANT EXECUTE ON FUNCTION public.enqueue_daily_sales_processing() TO service_role;
GRANT EXECUTE ON FUNCTION public.process_sales_processing_queue(integer) TO service_role;

-- Cron often runs as postgres; ensure it can use the queue and functions
GRANT SELECT, INSERT, UPDATE, DELETE ON public.sales_processing_queue TO postgres;
GRANT EXECUTE ON FUNCTION public.enqueue_daily_sales_processing() TO postgres;
GRANT EXECUTE ON FUNCTION public.process_sales_processing_queue(integer) TO postgres;

-- ============================================================
-- Cron: use queue instead of one big run
-- ============================================================
-- Remove old single-job cron so we don't run both (ignore error if job not found)
DO $$
BEGIN
  PERFORM cron.unschedule('daily-sales-processing');
EXCEPTION WHEN OTHERS THEN NULL;
END $$;

-- Enqueue all active dealers at 02:00 daily:
SELECT cron.schedule(
  'daily-sales-enqueue',
  '0 2 * * *',
  $$SELECT public.enqueue_daily_sales_processing()$$
);

-- Process one dealer per run (each dealer can have huge data). Every 2 min = 100 dealers in ~3.3 hours.
SELECT cron.schedule(
  'daily-sales-process-queue',
  '*/2 * * * *',
  $$SELECT public.process_sales_processing_queue(1)$$
);
